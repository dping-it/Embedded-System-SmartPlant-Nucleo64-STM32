
\ Definitions for a few Arduino pins on 
\ connectors CN5 and CN9:

: D0    3 GPIOA ;
: D2   10 GPIOA ; 
: D3    3 GPIOB ;
: D4    5 GPIOB ;
: D5    4 GPIOB ;
: D6   10 GPIOB ;
: D7    8 GPIOA ;
: D11   7 GPIOA ;
: D12   6 GPIOA ;
: D13   5 GPIOA ;
\ Examples:
\
\ TRUE D13 OUT!
\ Sets Arduino D13 pin (connector CN5) high
\ This is another way to turn the LD2 LED on.
\
